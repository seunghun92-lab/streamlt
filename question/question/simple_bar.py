import streamlit as st
import pandas as pd

# 가상 데이터 생성: 지역별 제품 판매량 데이터
data = pd.DataFrame({
    '지역': ['서울', '부산', '대구', '광주', '대전'],
    '제품 A 판매량': [250, 400, 320, 150, 270],
    '제품 B 판매량': [300, 500, 350, 180, 320],
    '제품 C 판매량': [450, 600, 520, 380, 410]
})

# 지역을 인덱스로 설정하여 막대 차트 표시
st.bar_chart(data.set_index('지역'))